module global_variables
    
    
!----------------------------------------------------------------------
! Time variables
!----------------------------------------------------------------------
    integer :: istime, ietime, ioutput
    real(8) :: dt, stime, etime, toutput
    real(8) :: ctime
    
    
!----------------------------------------------------------------------
! Grid
!----------------------------------------------------------------------
    
    integer :: i, j
    integer :: isize, jsize
    real(8), dimension(:,:), allocatable :: xx, yy, zz
    real(8), dimension(:,:), allocatable :: zc, sn
    integer, dimension(:,:), allocatable :: iobst
    
    real(8), dimension(:), allocatable :: zave
    
!----------------------------------------------------------------------
! Water flow
!----------------------------------------------------------------------
    real(8), dimension(:,:), allocatable :: depth, wl
    real(8), dimension(:,:), allocatable :: vx, vy

    
    
    
    contains
    
    
!======================================================================
! Default Values
!======================================================================
    subroutine set_default()
    
    
!----------------------------------------------------------------------
! Time variables
!----------------------------------------------------------------------
    dt = 0.1d0
    stime = 0.0d0
    etime = 3600.d0 * 24.0d0
    toutput = 60.0d0
    
    istime = int(stime/dt)
    ietime = int(etime/dt)
    ioutput = int(toutput/dt)
    
!----------------------------------------------------------------------
! Grid
!----------------------------------------------------------------------
    
    
    
!----------------------------------------------------------------------
! Water flow
!----------------------------------------------------------------------
    
    
    
    
    end subroutine
    
!======================================================================
! Default Values
!======================================================================
    subroutine alloc_variables(isize, jsize)
    
    
!----------------------------------------------------------------------
! Time variables
!----------------------------------------------------------------------
    
!----------------------------------------------------------------------
! Grid
!----------------------------------------------------------------------
    allocate(xx(1:isize, 1:jsize), yy(1:isize, 1:jsize), zz(1:isize, 1:jsize))    
    
    allocate(zc(1:isize-1, 1:jsize-1), sn(1:isize-1, 1:jsize-1))
    allocate(iobst(1:isize-1, 1:jsize-1))
    
    allocate(zave(1:isize-1))
    
!----------------------------------------------------------------------
! Water flow
!----------------------------------------------------------------------
    allocate(depth(1:isize-1, 1:jsize-1), wl(1:isize-1, 1:jsize-1))
    allocate(vx(1:isize, 1:jsize-1), vy(1:isize-1, 1:jsize))
    
    end subroutine
    
    
    
end module